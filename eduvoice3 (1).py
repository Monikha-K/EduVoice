def batch_process_files(file_type="text", num_files=3):
    """
    Process multiple files at once

    Args:
        file_type: 'pdf' or 'image'
        num_files: Number of files to upload
    """
    print("\n" + "="*70)
    print(f"BATCH PROCESSING: {num_files} {file_type.upper()} FILES")
    print("="*70)

    results = []

    for i in range(num_files):
        print(f"\nüì§ Upload file {i+1}/{num_files}:")
        uploaded = files.upload()

        if uploaded:
            filename = list(uploaded.keys())[0]
            print(f"\nüîÑ Processing {i+1}/{num_files}: {filename}")

            result = eduvoice.process(
                source=filename,
                source_type=file_type,
                max_length=150,
                min_length=50,
                style="concise",
                generate_audio=True,
                audio_output=f"{filename.split('.')[0]}_summary.mp3",
                lang='en'
            )

            results.append({
                'filename': filename,
                'result': result
            })

    print("\n" + "="*70)
    print(f"‚úÖ BATCH PROCESSING COMPLETE: {len(results)} files processed")
    print("="*70)

    return results

