import torch
import re
import pytesseract
from PIL import Image
import PyPDF2
from gtts import gTTS
from IPython.display import Audio, display

class EduVoicePipeline:
    """
    Complete end-to-end pipeline for educational content summarization
    Supports: Text, PDF, and Image inputs
    Outputs: Summaries with optional text-to-speech audio
    """

    def __init__(self, model_path=None):
        """
        Initialize pipeline with trained model

        Args:
            model_path: Path to saved model (default: use config.OUTPUT_DIR)
        """
        if model_path is None:
            model_path = config.OUTPUT_DIR

        print(f"Loading EduVoice model from: {model_path}")

        from transformers import AutoTokenizer, AutoModelForSeq2SeqLM

        self.tokenizer = AutoTokenizer.from_pretrained(model_path)
        self.model = AutoModelForSeq2SeqLM.from_pretrained(model_path)
        self.model.to(config.DEVICE)
        self.device = config.DEVICE

        print(f"‚úì Model loaded successfully on {self.device}")

    def extract_text_from_pdf(self, pdf_path):
        """Extract text from PDF file"""
        try:
            text = ""
            with open(pdf_path, 'rb') as file:
                pdf_reader = PyPDF2.PdfReader(file)
                num_pages = len(pdf_reader.pages)
                print(f"  Processing {num_pages} pages...")

                for i, page in enumerate(pdf_reader.pages, 1):
                    page_text = page.extract_text()
                    if page_text:
                        text += page_text + " "
                    if i % 5 == 0:
                        print(f"  Processed {i}/{num_pages} pages")

            return text.strip()
        except Exception as e:
            print(f"‚ùå Error extracting PDF: {e}")
            return ""

    def extract_text_from_image(self, image_path):
        """Extract text from image using OCR"""
        try:
            print("  Running OCR on image...")
            image = Image.open(image_path)
            text = pytesseract.image_to_string(image)
            return text.strip()
        except Exception as e:
            print(f"‚ùå Error extracting from image: {e}")
            return ""

    def clean_text(self, text):
        """Clean and preprocess text"""
        # Remove extra whitespace
        text = re.sub(r'\s+', ' ', text)
        # Remove special characters but keep punctuation
        text = re.sub(r'[^\w\s\.\,\!\?\-\(\)\:]', '', text)
        return text.strip()

    def simplify_for_audio(self, text):
        """Make text more audio-friendly"""
        replacements = {
            'e.g.': 'for example',
            'i.e.': 'that is',
            'etc.': 'and so on',
            'vs.': 'versus',
            'Dr.': 'Doctor',
            'Prof.': 'Professor',
            'Mr.': 'Mister',
            'Mrs.': 'Misses',
            'Ms.': 'Miss'
        }

        for abbr, full in replacements.items():
            text = text.replace(abbr, full)

        return text

    def summarize(self, text, max_length=100, min_length=40, style="concise"):
        """
        Generate summary from text

        Args:
            text: Input text to summarize
            max_length: Maximum summary length
            min_length: Minimum summary length
            style: 'concise' or 'detailed'
        """
        # Adjust parameters based on style
        if style == "detailed":
            max_length = int(max_length * 1.5)
            min_length = int(min_length * 1.2)

        # Tokenize
        inputs = self.tokenizer(
            text,
            max_length=512,
            truncation=True,
            return_tensors="pt"
        ).to(self.device)

        # Generate summary
        summary_ids = self.model.generate(
            inputs["input_ids"],
            max_length=max_length,
            min_length=min_length,
            num_beams=4,
            length_penalty=2.0,
            early_stopping=True
        )

        # Decode
        summary = self.tokenizer.decode(summary_ids[0], skip_special_tokens=True)
        return summary

    def text_to_audio(self, text, output_file="summary.mp3", lang='en', slow=False):
        """Convert text to audio file"""
        try:
            tts = gTTS(text=text, lang=lang, slow=slow)
            tts.save(output_file)
            print(f"  ‚úì Audio saved: {output_file}")
            return output_file
        except Exception as e:
            print(f"  ‚ùå Audio generation error: {e}")
            return None

    def play_audio(self, audio_file):
        """Play audio in Colab"""
        try:
            display(Audio(audio_file, autoplay=False))
        except Exception as e:
            print(f"  ‚ùå Audio playback error: {e}")

    def process(self, source, source_type="text", max_length=100,
                min_length=40, style="concise", generate_audio=True,
                audio_output="summary.mp3", lang='en'):
        """
        Complete processing pipeline

        Args:
            source: Text string, PDF path, or image path
            source_type: 'text', 'pdf', or 'image'
            max_length: Maximum summary length
            min_length: Minimum summary length
            style: 'concise' or 'detailed'
            generate_audio: Whether to generate audio output
            audio_output: Output audio filename
            lang: Language code for audio (en, hi, ta, es, fr, de, etc.)

        Returns:
            Dictionary with original text, summary, and audio file path
        """
        print("\n" + "="*70)
        print("üìö EDUVOICE - EDUCATIONAL CONTENT SUMMARIZATION")
        print("="*70)

        # Step 1: Extract text
        print("\n[1/4] üìÑ Extracting text...")
        if source_type == "pdf":
            text = self.extract_text_from_pdf(source)
        elif source_type == "image":
            text = self.extract_text_from_image(source)
        else:
            text = source

        if not text or len(text) < 20:
            print("‚ùå No text extracted or text too short!")
            return None

        print(f"  ‚úì Extracted {len(text)} characters")

        # Step 2: Clean text
        print("\n[2/4] üßπ Cleaning text...")
        cleaned_text = self.clean_text(text)
        print(f"  ‚úì Cleaned to {len(cleaned_text)} characters")

        # Step 3: Summarize
        print(f"\n[3/4] ü§ñ Generating {style} summary...")
        summary = self.summarize(
            cleaned_text,
            max_length=max_length,
            min_length=min_length,
            style=style
        )

        audio_friendly_text = self.simplify_for_audio(summary)

        word_count = len(summary.split())
        print(f"  ‚úì Summary generated ({word_count} words)")

        print("\n" + "="*70)
        print("üìù SUMMARY:")
        print("="*70)
        print(summary)
        print("="*70)

        # Step 4: Generate audio
        audio_file = None
        if generate_audio:
            print(f"\n[4/4] üîä Generating audio ({lang})...")
            audio_file = self.text_to_audio(
                audio_friendly_text,
                output_file=audio_output,
                lang=lang
            )

            if audio_file:
                print("  ‚úì Audio generated successfully!")
                print("\nüéµ Playing audio:")
                self.play_audio(audio_file)

        print("\n" + "="*70)
        print("‚úÖ PROCESSING COMPLETE!")
        print("="*70)

        return {
            'original_text': text,
            'cleaned_text': cleaned_text,
            'summary': summary,
            'audio_friendly_summary': audio_friendly_text,
            'audio_file': audio_file,
            'word_count': word_count,
            'compression_ratio': len(text) / len(summary) if summary else 0
        }

